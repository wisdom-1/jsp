package chap01.util;

public class Gugudan {
	
	public static void getGugudan() {
	for(int i =2;i<10; i++) {
		
		for (int j=1;j<9;j++) {
			System.out.println(i+"단을 출력하겠습니다.");
			System.out.println(i+"*"+j +"="+ i*j);
			
		}
		System.out.println(i+"단 출력 끝");
		
	}
	}

	
	

}
